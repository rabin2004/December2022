package oOPSconcept;

public class GrandParentClass {
	
	public void grandParentMethod1() {
		System.out.println("Grand Parent method-1");
	}
	
	public static void grandParentMethod2() {
		System.out.println("Grand Parent method-2");
	}
	
	public void overridenMethod() {
		System.out.println("Method overriding - Grand Parent Class");
	}

}
